package ForWhileLoops;

public class ForWhileLoops06 {




    /*

    Print Alphabet on the console.

    OUTPUT  : a b c .. .. .. .. y z

    */

    public static void main(String[] args) {


        for (char i = 'a'; i <='z' ; i++) {

            System.out.print(i+" ");

        }




    }




}
